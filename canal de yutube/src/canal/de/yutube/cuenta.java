package canal.de.yutube;

/**
 *
 * @author vina
 */
public class cuenta {
    private String nom_cuenta, contra_cuenta, correo_ceunta;

    public cuenta() {
    }

    public cuenta(String nom_cuenta, String contra_cuenta, String correo_ceunta) {
        this.nom_cuenta = nom_cuenta;
        this.contra_cuenta = contra_cuenta;
        this.correo_ceunta = correo_ceunta;
    }

    public String getNom_cuenta() {
        return nom_cuenta;
    }

    public void setNom_cuenta(String nom_cuenta) {
        this.nom_cuenta = nom_cuenta;
    }

    public String getContra_cuenta() {
        return contra_cuenta;
    }

    public void setContra_cuenta(String contra_cuenta) {
        this.contra_cuenta = contra_cuenta;
    }

    public String getCorreo_ceunta() {
        return correo_ceunta;
    }

    public void setCorreo_ceunta(String correo_ceunta) {
        this.correo_ceunta = correo_ceunta;
    }
    
    
    
    
    
    
    
}
